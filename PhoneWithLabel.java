public class PhoneWithLabel {

    private int number;
    private String typeOfPhone;

    public String getTypeOfPhone() {
        return typeOfPhone;
    }

    public int getNumber() {
        return number;
    }

    public PhoneWithLabel(int number, String typeOfPhone) {
        this.number = number;
        this.typeOfPhone = typeOfPhone;
    }

}
